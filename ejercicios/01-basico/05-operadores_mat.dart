main() {
  int a = 10 + 5;   // 15 +
  a = 20 - 10;      // 10 -
  a = 10 * 2;       // 20 *

  double b = 10 / 2;  // 5 /
  b = 10.0 % 3;       // 1 %
  b = -b;             // -expresión (usado para cambiar el valor de la expresión)

  int c = 10 ~/ 3;    // 3 ~/ División común y corriente (parte entera de la división)

  int d = 1;
  d++;                // 2 Incrementa en uno el valor
  d--;                // 1 Decrementa en uno el valor

  d += 2;             // 3 Incrementa en dos el valor
  d -= 2;             // 1 Decrementa en dos el valor
}